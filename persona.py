class Persona:
    def __init__(self, nombre, edad, altura, sexo):
        self._nombre = nombre
        self._edad = edad 
        self._altura = altura
        self._sexo = sexo

    def setNombre(self, nombre):
        self._nombre = nombre

    def getNombre(self):
        return self._nombre

    def setNombre(self, edad):
        self._edad = edad

    def getNombre(self):
        return self._edad

    def setNombre(self, altura):
        self._altura = altura

    def getNombre(self):
        return self._altura

    def setNombre(self, sexo):
        self._sexo = sexo

    def getNombre(self):
        return self._sexo